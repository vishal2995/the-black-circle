jQuery( document ).ready(function($) {

    $(".menu-toggle").click(function () {
		$("body").toggleClass("open-menu");
	})

	jQuery(function() {
		var header = jQuery(".header");
		jQuery(window).scroll(function() {    
			var scroll = jQuery(window).scrollTop();
			if (scroll >= 50) {
				header.addClass("sticky-header");
			} else {
				header.removeClass("sticky-header");
			}
		});
	});

	$('.video-box').each(function(){
		var $obj = $(this);
		var $videoBgAspect = $obj.find(".videobg-aspect");
		var $videoBgWidth = $obj.find(".videobg-width");
		var videoAspect = $videoBgAspect.outerHeight() / $videoBgAspect.outerWidth();

		windowAspect = ($(this).parent().height() / $(this).parent().width());
		if (windowAspect > videoAspect) {
			$videoBgWidth.width((windowAspect / videoAspect) * 101 + '%');
		} else {
			$videoBgWidth.width(101 + "%")
		}
	})

	const sliderTop = new Swiper(".platform-slider", {
		spaceBetween: 0,
		slidesPerView: 1,
		breakpoints: {
			767: { 
				slidesPerView: 2,
			},
			992: { 
				slidesPerView: 3,
			},
			1200: { 
				slidesPerView: 4,
			},
		},
		loop: true,
		loopedSlides: 4,

		navigation: {
			nextEl: ".swiper-button-next",
			prevEl: ".swiper-button-prev"
		},
	});

	const sliderBottom = new Swiper(".platform-content-slider", {
		slidesPerView: 1,
		loop: true,
		loopedSlides: 4
	});

	sliderTop.controller.control = sliderBottom;
	sliderBottom.controller.control = sliderTop;

	AOS.init({once: true});

});